int Ncr(int n,int r){
	int C[r+1];
	memset(C,0,sizeof(C));
	C[0]=1;
	for(int i=1;i<=n;i++){
		for(int j=min(i,r);j>0;j--){
			C[j]=(C[j]+C[j-1]);
		}
	}
	return C[r];
}


float num(string a,string b){
	int size=a.size();
	int sum=0;
	int sum2=0;
	int unk=0;
	for(int i=0;i<size;i++){
		if(a[i]=='+')
			sum++;
		else 
			sum--;
		if(b[i]=='+')
			sum2++;
		else if(b[i]=='-')
			sum2--;
		else
			unk++;	
	}

	if(unk==0){
		if(sum==sum2)
			return 1;
		else 
			return 0;
	}
	else{
		int edit=sum-sum2;
		if(abs(unk)<abs(edit)){
			return 0;
		}
		else{
			int alp=abs(edit);
			int rest=unk-alp;
			if(rest%2==0){
				int nminus;
				alp=alp+rest/2;
				nminus=rest/2;
				float Ncr2=Ncr(unk,alp);
				float total=pow(2,unk);
				return Ncr2/total;
			}
			else{
				return 0;
			}
		}
	}
}


